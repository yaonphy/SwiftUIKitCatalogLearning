import UIKit

// http://stackoverflow.com/questions/24142829/how-to-create-swift-class-for-category

extension UIColor{
    static var applicationGreenColor: UIColor{
        return UIColor(red: 0.255, green: 0.804, blue: 0.470, alpha: 1.0)
    }
    
    static var applicationPurpleColor: UIColor{
        return UIColor(red: 0.659, green: 0.271, blue: 0.988, alpha: 1)
    }
    
}
